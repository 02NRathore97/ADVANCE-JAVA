package in.com.raystec.marksheet;

public class MarksheetBean {
	
	//attributes
	private int id;
	private String rollno;
	private String fname;
	private String lname;
	private int phy;
	private int che;
	private int mat;
	
	

	//getter/setter
	
	
	//for id......
	public int getId() {
		return id;
	}
	public void setId(int id) {
		 this.id = id;
		}
	
	//for rollno.....
	public String getRollno() {
		return rollno;
	}
	public void setRollno(String rollno) {
		this.rollno = rollno;
	}
	
	//for fname......
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	
	//for lname......
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	
	//for phy......
	public int getPhy() {
		return phy;
	}
	public void setPhy(int phy) {
		this.phy = phy;
	}
	
	//for che.....
	public int getChe() {
		return che;
	}
	public void setChe(int che) {
		this.che = che;
	}
	
	//for mat......
	public int getMat() {
		return mat;
	}
	public void setMat(int mat) {
		this.mat = mat;
	}
}
